/*

---------Conditional Expressions------

  CASE/ IIF(T-SQL)
  ISNULL
  NULLIF

*/

-- CASE

SELECT
    PERSONEL_ADI,
	PERSONEL_SOYADI,
	UNVAN_ADI,
	CASE
	    WHEN UNVAN_ADI IN ('Müdür Yrd.','Genel Müdür Yrd.') THEN 'EVET'
		ELSE 'HAYIR'
	END
	AS YONETICI_MI
FROM IK_PERSONEL



-- IIF

SELECT
    PERSONEL_ADI,
	PERSONEL_SOYADI,
	UNVAN_ADI,

	IIF(UNVAN_ADI IN ('Müdür Yrd.','Genel Müdür Yrd.'),'EVET','HAYIR')
	
	AS YONETICI_MI
FROM IK_PERSONEL


-- ISNULL

SELECT 
    PERSONEL_ADI,
	PERSONEL_SOYADI,
	OZEL_TELEFON,
	IS_TELEFON,
	ISNULL(IS_TELEFON,OZEL_TELEFON) AS TELEFON_SON  --NULLSA ISE OZELÝ YAZDIRDI
    
	FROM IK_PERSONEL


-- NULLIF


SELECT 
    PERSONEL_ADI,
	PERSONEL_SOYADI,
	OZEL_TELEFON,
	IS_TELEFON,
	NULLIF(OZEL_TELEFON,IS_TELEFON) AS TELEFON --EÞÝTSE NULL GETÝRDÝ
    
	FROM IK_PERSONEL AS PER


